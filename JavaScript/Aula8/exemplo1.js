// OPERADORES RELACIONAIS

5 > 2 // true
7 < 4 // false
8 >= 8 // true
9 <= 7 // false
5 == 5 // true
4 != 4 // false

// EXEMPLOS
preco >= 200.50 // o preço é maior ou igual a R$200.5?
idade < 18 // a idade é menor do que 18?
curso == 'JavaScript' // o curso é JavaScript?
n1 != n2 // n1 é diferente de n2?

// IDENTIDADE
5 == 5 // true 
5 == '5' // true, tem a mesma grandeza
5 === '5' // false, são do mesmo valor, mas não são do mesmo tipo
5 === 5 // true