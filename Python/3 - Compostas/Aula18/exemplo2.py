# outra forma de criar lista dentro de lista
pessoas = [
    ['Pedro', 25], # pessoa 0
    ['Maria', 19], # pessoa 1
    ['João', 32] # pessoa 2
]

# COMPORTAMENTO DE MATRIZES
# busca na pessoa 0, a informação 0
print(pessoas [0][0]) # Pedro

# busca na pessoa 1, a informação 1
print(pessoas [1][1]) # 19

# busca na pessoa 2, a informação 0
print(pessoas [2][0]) # João

# busca pessoa 1
print(pessoas [1]) # ['Maria', 19]